// Write a Dart program to declare variables of type int, double, String, bool, and var, and print their values.

void main() {
  // Declare variables of different types
  int myInt = 42;
  double myDouble = 3.14;
  String myString = 'Hello, Dart!';
  bool myBool = true;
  var myVar = 'This is a variable of type var';

  // Print the values of the variables
  print(
      'Integer value: $myInt\nDouble value: $myDouble\nString value: $myString\nBoolean value: $myBool\nVar value: $myVar');
}
